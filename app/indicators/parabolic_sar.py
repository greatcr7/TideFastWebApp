        fig.add_trace(
            go.Candlestick(
                x=df['date'],
                open=df['open'],
                high=df['high'],
                low=df['low'],
                close=df['close'],
                name='‰ª∑Ê†º (Price)'
            ), 
            row=1, col=1
        )
        
        # EMAs
